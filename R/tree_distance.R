  }
  
  nSplits1 <- dimSplits1[2]
  nSplits2 <- dimSplits2[2]
  
  SymmetricDifference <- function (A, B) {
    (A & !B) | (!A & B)
    #TODO: Assert: These will always be the same size
    #Therefore: Improve efficiency by only calculating one, and not dividing by 2
  }
  
