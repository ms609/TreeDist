    min(
      sum(SymmetricDifference(A1, A2), SymmetricDifference(B1, B2)),
      sum(SymmetricDifference(A1, A2), SymmetricDifference(B1, B2))
    ) / 2L
      
  }, rep(seq_len(nSplits1), each=nSplits2), seq_len(nSplits2)
